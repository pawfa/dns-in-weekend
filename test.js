const {test} = require("node:test")
const {DNSPacket,DNSRecord, DNSHeader, DNSQuestion} = require ("./dns-models");
const {BufferReader} = require ("./reader");
const assert = require ("assert");


test("should correctly read www.yahoo.com dns response",()=> {
    const exampleYahooResp = Buffer.from([
        0x5a, 0xa8, 0x81, 0x80, 0x00, 0x01, 0x00, 0x05, 0x00, 0x00, 0x00, 0x00, 0x03, 0x77, 0x77, 0x77,
        0x05, 0x79, 0x61, 0x68, 0x6f, 0x6f, 0x03, 0x63, 0x6f, 0x6d, 0x00, 0x00, 0x01, 0x00, 0x01, 0xc0,
        0x0c, 0x00, 0x05, 0x00, 0x01, 0x00, 0x00, 0x01, 0x1c, 0x00, 0x0f, 0x06, 0x66, 0x64, 0x2d, 0x66,
        0x70, 0x33, 0x03, 0x77, 0x67, 0x31, 0x01, 0x62, 0xc0, 0x10, 0xc0, 0x2b, 0x00, 0x05, 0x00, 0x01,
        0x00, 0x00, 0x01, 0x1d, 0x00, 0x09, 0x06, 0x64, 0x73, 0x2d, 0x66, 0x70, 0x33, 0xc0, 0x32, 0xc0,
        0x46, 0x00, 0x05, 0x00, 0x01, 0x00, 0x00, 0x00, 0x2d, 0x00, 0x15, 0x0e, 0x64, 0x73, 0x2d, 0x61,
        0x6e, 0x79, 0x2d, 0x66, 0x70, 0x33, 0x2d, 0x6c, 0x66, 0x62, 0x03, 0x77, 0x61, 0x31, 0xc0, 0x36,
        0xc0, 0x5b, 0x00, 0x05, 0x00, 0x01, 0x00, 0x00, 0x01, 0x1d, 0x00, 0x12, 0x0f, 0x64, 0x73, 0x2d,
        0x61, 0x6e, 0x79, 0x2d, 0x66, 0x70, 0x33, 0x2d, 0x72, 0x65, 0x61, 0x6c, 0xc0, 0x6a, 0xc0, 0x7c,
        0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x2d, 0x00, 0x04, 0xce, 0xbe, 0x24, 0x2d
    ]);

    const packet = DNSPacket.readFromBytes(new BufferReader(exampleYahooResp))
    const header = new DNSHeader(23208, 1);
    header.response = 1;
    header.recursion_available = 1;
    header.answers = 5;

    const question = new DNSQuestion("www.yahoo.com",1,1)

    assert.deepStrictEqual(packet, new DNSPacket(header, [question],
        [
        new DNSRecord('www.yahoo.com', 'CNAME', 1, 284, 15, undefined, 'fd-fp3.wg1.b.yahoo.com'),
        new DNSRecord('fd-fp3.wg1.b.yahoo.com', 'CNAME', 1, 285, 9, undefined, 'ds-fp3.wg1.b.yahoo.com'),
        new DNSRecord('ds-fp3.wg1.b.yahoo.com', 'CNAME', 1, 45, 21, undefined, 'ds-any-fp3-lfb.wa1.b.yahoo.com'),
        new DNSRecord('ds-any-fp3-lfb.wa1.b.yahoo.com', 'CNAME', 1, 285, 18, undefined, 'ds-any-fp3-real.wa1.b.yahoo.com'),
        new DNSRecord('ds-any-fp3-real.wa1.b.yahoo.com', 'A', 1, 45, 4, '206.190.36.45', undefined),
        ]
    ))
})